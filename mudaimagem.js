function mudaimagem(){
    var primimg = document.querySelector("#primeiraimg");
    var segimg= document.querySelector("#segundaimg");
    primimg.style.display= "none";
    segimg.style.display= "block";
}
function voltaimagem(){
    var primimg = document.querySelector("#primeiraimg");
    var segimg= document.querySelector("#segundaimg");
    primimg.style.display= "block";
    segimg.style.display= "none";
}
